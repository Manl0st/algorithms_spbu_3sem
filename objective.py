"""
objective.py
============
Целевая функция Eggholder для задачи глобальной оптимизации.

Функция имеет множество локальных минимумов и один глобальный минимум,
что делает её хорошим тестовым случаем для алгоритмов глобальной оптимизации.

Ссылка: https://www.sfu.ca/~ssurjano/egg.html
"""

import math


# ---------------------------------------------------------------------------
# Границы поиска для обеих переменных
# ---------------------------------------------------------------------------
BOUNDS = [(-512.0, 512.0), (-512.0, 512.0)]

# ---------------------------------------------------------------------------
# Истинный глобальный минимум Eggholder (справочное значение)
# ---------------------------------------------------------------------------
TRUE_MIN = (512.0, 404.2319)   # (x*, y*)
TRUE_F   = -959.6407            # f(x*, y*)


# ---------------------------------------------------------------------------
# Целевая функция
# ---------------------------------------------------------------------------

def eggholder(x: float, y: float) -> float:
    """
    Функция Eggholder — нелинейная, многомодальная тестовая функция.

    f(x,y) = -(y+47)*sin(sqrt(|x/2 + (y+47)|)) - x*sin(sqrt(|x - (y+47)|))

    Параметры
    ---------
    x, y : float
        Координаты точки. Рекомендуемая область: x,y ∈ [-512, 512].

    Возвращает
    ----------
    float
        Значение функции в точке (x, y).
    """
    term1 = -(y + 47.0) * math.sin(math.sqrt(abs(x / 2.0 + (y + 47.0))))
    term2 = -x * math.sin(math.sqrt(abs(x - (y + 47.0))))
    return term1 + term2


# ---------------------------------------------------------------------------
# Обёртка для подсчёта вычислений целевой функции
# ---------------------------------------------------------------------------

class CounterObjective:
    """
    Обёртка над целевой функцией, которая считает число её вычислений.

    Пример использования
    --------------------
    >>> obj = CounterObjective()
    >>> val = obj(1.0, 2.0)
    >>> print(obj.evals)  # 1
    >>> obj.reset()
    >>> print(obj.evals)  # 0
    """

    def __init__(self):
        # Счётчик вычислений функции
        self.evals: int = 0

    def __call__(self, x: float, y: float) -> float:
        """
        Вычислить Eggholder(x, y) и увеличить счётчик на 1.

        Параметры
        ---------
        x, y : float
            Аргументы целевой функции.

        Возвращает
        ----------
        float
            Значение eggholder(x, y).
        """
        self.evals += 1
        return eggholder(x, y)

    def reset(self) -> None:
        """Обнулить счётчик вычислений."""
        self.evals = 0
